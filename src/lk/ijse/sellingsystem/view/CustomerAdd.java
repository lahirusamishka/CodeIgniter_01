    private void loadAllCustomers() {

        try {
            
            try {
                ArrayList<CustomerDTO> allCustomers = CustomerAddController.getAllCustomers();
                
                DefaultTableModel dtm = (DefaultTableModel) tblCustomerView.getModel();
                
                dtm.setRowCount(0);
                
                if (allCustomers != null) {
                    
                    for (CustomerDTO customer : allCustomers) {
                        
                        Object[] rowData = {
                            customer.getId(),
                            customer.getName(),
                            customer.getAddress(),
                            customer.getAdvance()
                            
                            
                            
                            ,};
                        
                        dtm.addRow(rowData);
                        
                    }
                    
                }
            } catch (Exception ex) {
                Logger.getLogger(CustomerAdd.class.getName()).log(Level.SEVERE, null, ex);
            }
            
            
            
            ArrayList<VehicleDTO>allVehicles=VehicleController.getAllVehicles();
            
            cmbVehicleId.removeAllItems();
            if(allVehicles==null) return;
            
            for (VehicleDTO allVehicle : allVehicles) {
                cmbVehicleId.addItem(allVehicle.getId());
            }
           
            
        } catch (Exception ex) {
            Logger.getLogger(CustomerAdd.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
        
        
        
        
        
        
    }

