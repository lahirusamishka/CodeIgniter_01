    private void loadDetails() {
        try {
            DefaultTableModel model = (DefaultTableModel) tblCustomerView.getModel();
            model.setRowCount(0);
            ArrayList<CustomerDTO> allCustomers = CustomerAddController.getAllCustomers();

            for (CustomerDTO allCustomer : allCustomers) {
                Object[] rowData = {
                    allCustomer.getId(),
                    allCustomer.getAddress(),
                    allCustomer.getName()
                   
                };
                model.addRow(rowData);
            }
        } catch (Exception ex) {
            Logger.getLogger(CustomerAdd.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
