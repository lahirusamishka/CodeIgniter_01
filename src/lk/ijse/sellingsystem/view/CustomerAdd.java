    private void tblCustomerViewMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblCustomerViewMouseClicked
        try {
            String itemId = (String) tblCustomerView.getValueAt(tblCustomerView.getSelectedRow(), 0);
            CustomerDTO customerDTO = new CustomerDTO();
            customerDTO.setId(itemId);
            CustomerDTO searchCustomer = CustomerAddController.searchCustomer(customerDTO);
            txtCustomerID.setText(searchCustomer.getId());
            txtCustomerNIC.setText(searchCustomer.getNic());
            txtCustomeName.setText(searchCustomer.getName());
            txtCustomerAddress.setText(searchCustomer.getAddress());
            txtEmail.setText(searchCustomer.getEmail());
            txtPhoneno.setText(searchCustomer.getPhoneNo());
            cmbVehicleId.setSelectedItem(searchCustomer.getVehicleId());
            txtAdvancePayment.setText(searchCustomer.getAdvance().toString());
            txtDate.setText(searchCustomer.getDate());

        } catch (Exception ex) {
            Logger.getLogger(CustomerAdd.class.getName()).log(Level.SEVERE, null, ex);
        }


    }//GEN-LAST:event_tblCustomerViewMouseClicked

    private void btnSaveMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnSaveMouseClicked
        String supplierName = txtCustomeName.getText();

        if (!supplierName.matches("[A-Za-z. ]+")) {
            JOptionPane.showMessageDialog(this, "Please enter valid supplier name");
            txtCustomeName.selectAll();
            txtCustomeName.requestFocusInWindow();
            return;
        }
        String supplierNIC = txtCustomerNIC.getText();

        if (!supplierNIC.matches("\\d{9}[Vv]")) {
            JOptionPane.showMessageDialog(this, "Please enter valid supplier nic");
            txtCustomerNIC.selectAll();
            txtCustomerNIC.requestFocusInWindow();
            return;
        }

        String supplierContactNumber = txtPhoneno.getText();

        if (!supplierContactNumber.matches("\\d{3}\\d{7}")) {
            JOptionPane.showMessageDialog(this, "Please enter valid supplier contact number");
            txtPhoneno.selectAll();
            txtPhoneno.requestFocusInWindow();
            return;
        }
        //save button
        try {
            CustomerDTO customer;
            customer = new CustomerDTO(
                    txtCustomerID.getText(),
                    txtCustomerNIC.getText(),
                    txtCustomeName.getText(),
                    txtCustomerAddress.getText(),
                    txtPhoneno.getText(),
                    cmbVehicleId.getSelectedItem().toString(),
                    new BigDecimal(txtAdvancePayment.getText()),
                    txtDate.getText(),
                    txtEmail.getText()
            );

            boolean result = CustomerAddController.addCustomer(customer);

            if (result) {
                JOption mass = new JOption();
                mass.setVisible(true);

            } else {
                JOptionPane.showMessageDialog(this, "Customer hasn't been added due to some error");
            }
        } catch (Exception ex) {
            Logger.getLogger(CustomerAdd.class.getName()).log(Level.SEVERE, null, ex);
        }
        loadAllCustomers();
    }//GEN-LAST:event_btnSaveMouseClicked

