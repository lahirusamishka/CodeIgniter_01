    private void loadVehicleId() {

        try {
            String orderId;
            orderId = IDGenerator.getNewVehicleID("vehicle", "id", "VI");
            txtVehicleID.setText(orderId);
        } catch (SQLException ex) {
            Logger.getLogger(VehicleStore.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

    private void loadAllVehicles() {
        try {
            ArrayList<VehicleDTO> allCustomers = VehicleController.getAllVehicles();

            DefaultTableModel dtm = (DefaultTableModel) tblVehicleView.getModel();

            dtm.setRowCount(0);

            if (allCustomers != null) {

                for (VehicleDTO customer : allCustomers) {

                    Object[] rowData = {
                        customer.getId(),
                        customer.getBrand(),
                        customer.getPrice(),};

                    dtm.addRow(rowData);

                }

            }
        } catch (Exception ex) {
            Logger.getLogger(CustomerAdd.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

