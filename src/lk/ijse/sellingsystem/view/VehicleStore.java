    private void tblVehicleViewMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblVehicleViewMouseClicked
        try {
            String vehicleId=(String) tblVehicleView.getValueAt(tblVehicleView.getSelectedRow(),0);
            VehicleDTO cuVehicleDTO=new VehicleDTO();
            cuVehicleDTO.setId(vehicleId);
            VehicleDTO searchVehicle=VehicleController.searchVehicle(cuVehicleDTO);
            txtVehicleID.setText(searchVehicle.getId());
            cmbVehicleBrand.setSelectedItem(searchVehicle.getBrand());
            cmbManufacturer.setSelectedItem(searchVehicle.getManuYear());
            cmbVehicleType.setSelectedItem(searchVehicle.getType());
            txtVehiclePrice.setText(searchVehicle.getPrice().toString());
            cmbColor.setSelectedItem(searchVehicle.getColor());
            txtDesc.setText(searchVehicle.getDescription());
            
           
        } catch (Exception ex) {
            Logger.getLogger(VehicleStore.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_tblVehicleViewMouseClicked

