    private void btnSaveMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnSaveMouseClicked

        try {
            VehicleDTO vehicle;
            vehicle = new VehicleDTO(
                    txtVehicleID.getText(),
                    cmbVehicleType.getSelectedItem().toString(),
                    cmbVehicleBrand.getSelectedItem().toString(),
                    new BigDecimal(txtVehiclePrice.getText()),
                    cmbManufacturer.getSelectedItem().toString(),
                    cmbColor.getSelectedItem().toString(),
                    txtDesc.getText()
            );
            boolean result = VehicleController.addVehicle(vehicle);

            if (result) {
                SupplierJOption  mass = new SupplierJOption();
                mass.setVisible(true);

            } else {
                JOptionPane.showMessageDialog(this, "Customer hasn't been added due to some error");
            }

        } catch (Exception ex) {
            Logger.getLogger(VehicleStore.class.getName()).log(Level.SEVERE, null, ex);
        }

        loadAllVehicles();


    }//GEN-LAST:event_btnSaveMouseClicked

