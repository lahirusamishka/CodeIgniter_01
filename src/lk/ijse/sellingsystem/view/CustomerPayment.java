    private void loadPayment() {
         
            try {
                ArrayList<PaymentDTO> payment = CustomerPaymentController.getAllCustomers();
                
                DefaultTableModel dtm = (DefaultTableModel) tblPaymentView.getModel();
                
                dtm.setRowCount(0);
                
                if (payment != null) {
                    
                    for (PaymentDTO customer : payment) {
                        
                        Object[] rowData = {
                            customer.getId(),
                            
                            customer.getDate(),
                            customer.getPaymentId(),
                              cmbPaymentType.getSelectedItem(),
                        
                        };
                        
                        dtm.addRow(rowData);
                        
                    }
                    
                }
            } catch (Exception ex) {
                Logger.getLogger(CustomerAdd.class.getName()).log(Level.SEVERE, null, ex);
            }
    }
    
    
