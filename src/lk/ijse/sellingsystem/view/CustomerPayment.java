    private void btnSaveMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnSaveMouseClicked
        
        try {
            PaymentDTO paymentDTO;
            paymentDTO = new PaymentDTO(
                    txtPaymentId.getText(),
                    txtDate.getText(),
                    txtPaymentTypeId.getText()
            );
            
            PaymentTypeDTO paymentTypeDTO=new PaymentTypeDTO(txtPaymentTypeId.getText(), cmbPaymentType.getSelectedItem().toString());

            boolean result = PaymentController.addPayment(paymentDTO,paymentTypeDTO);

            if (result) {
                JOptionPane.showMessageDialog(this, "Payment has been successfully added.");
                loadPayment();
            } else {
                JOptionPane.showMessageDialog(this, "Payment hasn't been added due to some error");
            }
        } catch (Exception ex) {
            Logger.getLogger(CustomerPayment.class.getName()).log(Level.SEVERE, null, ex);
        }
       
    }//GEN-LAST:event_btnSaveMouseClicked

